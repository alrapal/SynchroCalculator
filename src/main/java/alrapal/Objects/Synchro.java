package alrapal.Objects;
import alrapal.Exceptions.InvalidBoostException;

public class Synchro {
    ////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////ATTRIBUTES////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////

    private float boost;
    private float baseDamage;

    ////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////CONSTRUCTORS//////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////

    public Synchro()
    {   this.boost = 0;
        this.baseDamage = 368; }

    ////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////GETTERS & SETTERS/////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////

    public float getBoost() {return boost;}
    public void setBoost(float boost) throws InvalidBoostException {
        if (checkBoostCountFormat(boost)){
            this.boost = boost;
        }else{
            throw new InvalidBoostException();
        }
    }
    public float getBaseDamage() {return baseDamage;}
    public void setBaseDamage(float baseDamage) {this.baseDamage = baseDamage;}

    ////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////METHODS///////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////

    private boolean checkBoostCountFormat(float boost)
    {  if (boost % 200 == 0)
    {   return true;
    }   else {
        return false;}
    }
}
